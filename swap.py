# Comparison operators
x = 10
y = 20
# Equal to
print("Equal to:", x == y)
# Not equal to
print("Not equal to:", x != y)
# Greater than
print("Greater than:", x > y)
# Less than
print("Less than:", x < y)
# Greater than or equal to
print("Greater than or equal to:", x >= y)
# Less than or equal to
print("Less than or equal to:", x <= y)